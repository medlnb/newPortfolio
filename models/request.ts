import { model, models, Schema } from "mongoose";

const requestSchema = new Schema({
  name: {
    type: String,
    required: true,
  },
  email: {
    type: String,
    required: true,
  },
  phone: {
    type: String,
    required: true,
  },
  message: {
    type: String,
    required: true,
  },
});

const Request = models.Request || model("Request", requestSchema);

export default Request;
